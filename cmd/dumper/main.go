package main

import (
	"encoding/binary"
	"os"
)

func main() {
	bytes := []byte{
		0x0, 0x0, 0x0, 0x3,
		0x0, 0x0, 0x0, 0x1,
		0x0, 0x0, 0x0, 0x3,
		0x0, 0x0, 0x0, 0x1,
		0x0, 0x0, 0x0, 0x5,
		0x0, 0x0, 0x0, 0x3,
		0x0, 0x0, 0x0, 0x30,
		0x0, 0x0, 0x0, 0x5,
		0x0, 0x0, 0x0, 0x2,
		0x0, 0x0, 0x0, 0x0,
	}
	outFile, err := os.Create("output.bin")
	if err != nil {
		panic(err)
	}
	err = binary.Write(outFile, binary.BigEndian, bytes)
	if err != nil {
		panic(err)
	}
}
